package golang

import (
	"bytes"
	"path/filepath"
	"sort"
	"text/template"

	"github.com/go-spring/gs-http-gen/gen/generator"
	"github.com/go-spring/gs-http-gen/lib/pathidl"
	"github.com/go-spring/stdlib/errutil"
)

// serverTmpl for generating the HTTP server wrapper based on RPC definitions.
var serverTmpl = template.Must(template.New("server").
	Funcs(map[string]any{
		"formatComments": formatComments,
		"sortRPCsByPath": func(inRPCs []RPC) []RPC {
			retRPCs := append([]RPC{}, inRPCs...)
			sort.Slice(retRPCs, func(i, j int) bool {
				a := retRPCs[i].PathSegments
				b := retRPCs[j].PathSegments

				minLen := len(a)
				if len(b) < minLen {
					minLen = len(b)
				}

				for k := 0; k < minLen; k++ {
					ak, bk := a[k].Type, b[k].Type
					if ak != bk {
						return ak < bk // Static(0) < Param(1) < Wildcard(2)
					}
					if ak == pathidl.Static {
						if a[k].Value != b[k].Value {
							return a[k].Value < b[k].Value
						}
					}
				}

				if len(a) != len(b) {
					return len(a) > len(b)
				}
				return retRPCs[i].Path < retRPCs[j].Path
			})
			return retRPCs
		},
	}).Parse(`
// Code generated by gs-http-gen compiler. DO NOT EDIT.

package {{.Package}}

import (
	"context"
	"net/http"

	"github.com/go-spring/stdlib/httpsvr"
)

// {{.Service}}Service defines the interface that service must implement.
type {{.Service}}Service interface {
    {{- range $r := .RPCs }}
		{{- if $r.Comments.Exists }}
			{{formatComments $r.Comments}}
		{{- end}}
		{{- if $r.SSE }}
			{{$r.Name}}(context.Context, *{{$r.Request}}, chan<- *httpsvr.Event[{{$r.Response}}])
		{{- else}}
			{{$r.Name}}(context.Context, *{{$r.Request}}) {{$r.Response}}
		{{- end}}
    {{- end}}
}

// Routers returns a list of HTTP routers for the service.
func Routers(server {{.Service}}Service, fn httpsvr.NewRequestContext) []httpsvr.Router {
	return []httpsvr.Router{
		{{- $tmpRPCs := sortRPCsByPath .RPCs}}
		{{- range $r := $tmpRPCs }}
			{
				Method:  "{{$r.Method}}",
				Pattern: "{{$r.Path}}",
				{{- if $r.SSE }}
					Handler: func(w http.ResponseWriter, r *http.Request) {
						ctx := httpsvr.WithRequestContext(r.Context(), fn(r, w))
						httpsvr.HandleStream(w, r.WithContext(ctx), New{{$r.Request}}(), server.{{$r.Name}})
					},
				{{- else}}
					Handler: func(w http.ResponseWriter, r *http.Request) {
						ctx := httpsvr.WithRequestContext(r.Context(), fn(r, w))
						httpsvr.HandleJSON(w, r.WithContext(ctx), New{{$r.Request}}(), server.{{$r.Name}})
					},
				{{- end}}
			},
	    {{- end}}
	}
}
`))

// genServer generates the HTTP handler and router initialization code
// for the given service context and RPC definitions.
func (g *Generator) genServer(config *generator.Config, spec GoSpec) error {
	buf := &bytes.Buffer{}
	err := serverTmpl.Execute(buf, map[string]any{
		"Package": config.GoPackage,
		"Service": spec.Meta.Name,
		"RPCs":    spec.RPCs,
	})
	if err != nil {
		return errutil.Explain(nil, "execute server template error: %w", err)
	}
	fileName := filepath.Join(config.OutputDir, "server.go")
	return formatFile(fileName, buf.Bytes())
}
