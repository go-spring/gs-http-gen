//go:embed proto
var protoDir embed.FS

// serverTmpl for generating the HTTP server wrapper based on RPC definitions.
var serverTmpl = template.Must(template.New("server").Parse(`
// Code generated by gs-http-gen compiler. DO NOT EDIT.

package {{.Package}}

import (
	"context"
	"net/http"
)

// {{.Service}}Server defines the interface that service must implement.
type {{.Service}}Server interface {
    {{- range $r := .RPCs}}
		{{- if $r.Comment}}
			{{$r.Comment}}
		{{- end}}
		{{- if $r.Stream}}
            {{$r.Name}}(context.Context, *{{$r.Request}}, chan<- *{{$r.Response}})
		{{- else}}
            {{$r.Name}}(context.Context, *{{$r.Request}}) *{{$r.Response}}
        {{- end}}
    {{- end}}
}

// InitRouter registers the service handlers into the given *http.ServeMux.
func InitRouter(mux *http.ServeMux, server {{.Service}}Server) {
    {{- range $r := .RPCs}}
		{{- if $r.Stream}}
			mux.HandleFunc("{{$r.Method}} {{$r.Path}}", HandleStream(server.{{$r.Name}}))
		{{- else}}
			mux.HandleFunc("{{$r.Method}} {{$r.Path}}", HandleJSON(server.{{$r.Name}}))
		{{- end}}
    {{- end}}
}
`))

// genServer generates the HTTP handler and router initialization code
// for the given service context and RPC definitions.
func (g *Generator) genServer(ctx Context, rpcs []tidl.RPC) error {

	// Copy proto template files to output directory
	{
		entries, err := protoDir.ReadDir("proto")
		if err != nil {
			return fmt.Errorf("read proto directory error: %w", err)
		}
		for _, e := range entries {
			if e.IsDir() {
				continue
			}
			b, err := protoDir.ReadFile(filepath.Join("proto", e.Name()))
			if err != nil {
				return fmt.Errorf("read proto file %s error: %w", e.Name(), err)
			}
			b = bytes.ReplaceAll(b, []byte("PACKAGE_NAME"), []byte(ctx.config.GoPackage))
			fileName := filepath.Join(ctx.config.OutputDir, e.Name())
			if err = os.WriteFile(fileName, b, os.ModePerm); err != nil {
				return fmt.Errorf("write proto file %s error: %w", fileName, err)
			}
		}
	}

	// Generate a Go file containing the HTTP server wrapper
	newRPCs, err := convertRPCs(rpcs)
	if err != nil {
		return fmt.Errorf("convert RPCs error: %w", err)
	}
	buf := &bytes.Buffer{}
	err = serverTmpl.Execute(buf, map[string]any{
		"Package": ctx.config.GoPackage,
		"Service": ctx.meta.Name,
		"RPCs":    newRPCs,
	})
	if err != nil {
		return fmt.Errorf("execute template error: %w", err)
	}
	fileName := ctx.meta.Name + "_http.go"
	fileName = filepath.Join(ctx.config.OutputDir, fileName)
	return formatFile(fileName, buf.Bytes())
}

// RPC represents a single remote procedure call with HTTP metadata.
type RPC struct {
	Name     string // Method name
	Request  string // Request type name
	Response string // Response type name
	Stream   bool   // Whether this RPC is a streaming RPC
	Path     string // HTTP path
	Method   string // HTTP method (GET, POST, etc.)
	Comment  string // Comment of the RPC
}

// convertRPCs converts tidl.RPC definitions into internal RPC structs.
func convertRPCs(rpcs []tidl.RPC) ([]RPC, error) {
	var ret []RPC
	for _, r := range rpcs {

		// Retrieve the required "path" annotation
		path, ok := tidl.GetAnnotation(r.Annotations, "path")
		if !ok {
			return nil, fmt.Errorf(`annotation "path" not found in rpc %s`, r.Name)
		}
		if path.Value == nil {
			return nil, fmt.Errorf(`annotation "path" value is nil in rpc %s`, r.Name)
		}

		// Retrieve the required "method" annotation
		method, ok := tidl.GetAnnotation(r.Annotations, "method")
		if !ok {
			return nil, fmt.Errorf(`annotation "method" not found in rpc %s`, r.Name)
		}
		if method.Value == nil {
			return nil, fmt.Errorf(`annotation "method" value is nil in rpc %s`, r.Name)
		}

		ret = append(ret, RPC{
			Name:     r.Name,
			Request:  r.Request.Name,
			Response: r.Response.UserType.Name,
			Stream:   r.Response.Stream,
			Path:     strings.Trim(*path.Value, `"`),
			Method:   strings.ToUpper(strings.Trim(*method.Value, `"`)),
			Comment:  formatComment(r.Comments),
		})
	}
	return ret, nil
}
