package golang

import (
	"bytes"
	"path/filepath"
	"text/template"

	"github.com/go-spring/gs-http-gen/gen/generator"
	"github.com/lvan100/golib/errutil"
)

// toolVersionTmpl for generating the tool version constant.
var toolVersionTmpl = template.Must(template.New("toolVersion").Parse(`
// Code generated by gs-http-gen compiler. DO NOT EDIT.

package {{.Package}}

const ToolVersion = "{{.ToolVersion}}"
`))

// genVersion generates a Go source file corresponding to the tool version.
func (g *Generator) genVersion(config *generator.Config, spec GoSpec) error {
	buf := &bytes.Buffer{}
	err := toolVersionTmpl.Execute(buf, map[string]any{
		"Package":     config.GoPackage,
		"ToolVersion": config.ToolVersion,
	})
	if err != nil {
		return errutil.Explain(nil, "generate tool version file error: %w", err)
	}
	fileName := filepath.Join(config.OutputDir, "version.go")
	return formatFile(fileName, buf.Bytes())
}
