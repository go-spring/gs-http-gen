package golang

import (
	"bytes"
	"path/filepath"
	"text/template"

	"github.com/go-spring/gs-http-gen/gen/generator"
	"github.com/lvan100/golib/errutil"
)

// clientTmpl is the template for the HTTP client code.
var clientTmpl = template.Must(template.New("client").
	Funcs(map[string]any{
		"formatComments": formatComments,
	}).Parse(`
// Code generated by gs-http-gen compiler. DO NOT EDIT.

package {{.Package}}

import (
	"bytes"
	"context"
	"encoding/json"
	"fmt"
	"net/http"
	"reflect"

	"github.com/go-spring/gs-http-gen/lib/httputil"
	"github.com/go-spring/gs-mock/gsmock"
)

var clientType = reflect.TypeFor[Client]()

// Client is a wrapper struct that embeds ClientInterface.
// It can be used to track or extend the construction process of the client.
type Client struct {
	Target string
}

{{- range $r := .RPCs}}
	{{- if $r.Comments.Exists }}
		{{formatComments $r.Comments}}
	{{- end}}
	{{- $respType := $r.Response }}
func (c *Client) {{$r.Name}}(ctx context.Context, req *{{$r.Request}}, opts ...httputil.RequestOption) (*http.Response, *{{$respType}}, error) {
	if ret, ok := gsmock.InvokeContext(ctx, clientType, "{{$r.Name}}", ctx, req, opts); ok {
		return gsmock.Unbox3[*http.Response, *{{$respType}}, error](ret)
	}

	path := fmt.Sprintf("{{$r.FormatPath}}", {{- range $unused, $p := $r.PathParams}} req.{{$p}}, {{- end}})
	if s, err := req.QueryForm(); err != nil {
		return nil, nil, err
	} else if s != "" {
		path += "?" + s
	}

	{{if eq $r.ContentType "application/json"}}
		buf := bytes.NewBuffer(nil)
		if err := json.NewEncoder(buf).Encode(req.{{$r.Request}}Body); err != nil {
			return nil, nil, err
		}
	{{- else}}
		var buf *bytes.Buffer
		if s, err := req.{{$r.Request}}Body.EncodeForm(); err != nil {
			return nil, nil, err
		} else if s != "" {
			buf = bytes.NewBufferString(s)
		}
	{{- end}}

	r, err := http.NewRequestWithContext(ctx, "{{$r.Method}}", path, buf)
	if err != nil {
		return nil, nil, err
	}

	r.Header.Set("Content-Type", "{{$r.ContentType}}")

	opts = append(opts, httputil.WithTarget(c.Target))
	opts = append(opts, httputil.WithPath("{{$r.Path}}"))
	opts = append(opts, httputil.WithSchema("http"))

	return httputil.JSONResponse[{{$r.Response}}](r, opts...)
}
{{end}}
`))

// genClient generates the HTTP client code for a given service.
func (g *Generator) genClient(config *generator.Config, spec GoSpec) error {
	buf := &bytes.Buffer{}
	err := clientTmpl.Execute(buf, map[string]any{
		"Package": config.GoPackage,
		"RPCs":    spec.RPCs,
		"SSEs":    spec.SSEs,
	})
	if err != nil {
		return errutil.Explain(nil, "execute template error: %w", err)
	}
	fileName := filepath.Join(config.OutputDir, "client.go")
	return formatFile(fileName, buf.Bytes())
}
