package golang

import (
	"bytes"
	"fmt"
	"path/filepath"
	"strings"
	"text/template"

	"github.com/go-spring/gs-http-gen/gen/generator"
	"github.com/lvan100/golib/errutil"
)

// clientTmpl is the template for the HTTP client code.
var clientTmpl = template.Must(template.New("client").
	Funcs(map[string]any{
		"formatComments": formatComments,
		"makeRespReturn": func(r RPC) string {
			if r.RespTypeKind[0] == TypeKindStructPtr {
				s := strings.TrimPrefix(r.Response, "*")
				return fmt.Sprintf("httpclt.ObjectResponse(ctx, New%s(), meta)", s)
			}
			return fmt.Sprintf("httpclt.JSONResponse[%s](ctx, meta)", r.Response)
		},
	}).Parse(`
// Code generated by gs-http-gen compiler. DO NOT EDIT.

package {{.Package}}

import (
	"context"
	"fmt"
	"net/http"

	"github.com/go-spring/gs-http-gen/lib/httpclt"
)

// Client represents an HTTP client bound to a specific service endpoint.
//
// Target identifies the remote service, which can be either a service name
// (for service discovery) or a direct address in the form of "IP:PORT".
type Client struct {
	Target string
}

{{- range $r := .RPCs }}
	{{- if not $r.SSE }}
		{{- if $r.Comments.Exists }}
			{{formatComments $r.Comments}}
		{{- end}}
		func (c *Client) {{$r.Name}}(ctx context.Context, req *{{$r.Request}}, opts ...httpclt.RequestOption) (*http.Response, {{$r.Response}}, error) {
			meta := httpclt.CombineMetadata(httpclt.Metadata{
				Target:  c.Target,
				Schema:  "http",
				Method:  "{{$r.Method}}",
				Pattern: "{{$r.Path}}",
				RawPath: fmt.Sprintf("{{$r.FormatPath}}", {{- range $unused, $p := $r.PathParams }} req.{{$p}}, {{- end}}),
				Query:   req,
				Body:    req.{{$r.Request}}Body,
				Header:  http.Header{"Content-Type": []string{"{{$r.ContentType}}"}},
			}, opts...)
			return {{makeRespReturn $r}}
		}
	{{- end}}
{{end}}
`))

// genClient generates the HTTP client code for a given service.
func (g *Generator) genClient(config *generator.Config, spec GoSpec) error {
	buf := &bytes.Buffer{}
	err := clientTmpl.Execute(buf, map[string]any{
		"Package": config.GoPackage,
		"RPCs":    spec.RPCs,
	})
	if err != nil {
		return errutil.Explain(nil, "execute client template error: %w", err)
	}
	fileName := filepath.Join(config.OutputDir, "client.go")
	return formatFile(fileName, buf.Bytes())
}
