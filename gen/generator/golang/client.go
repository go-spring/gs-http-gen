package golang

import (
	"bytes"
	"path/filepath"
	"text/template"

	"github.com/go-spring/gs-http-gen/gen/generator"
	"github.com/lvan100/golib/errutil"
)

var clientTmpl = template.Must(template.New("client").Parse(`
// Code generated by gs-http-gen compiler. DO NOT EDIT.

package {{.Package}}

import (
	"context"
	"fmt"
	"net/http"
	"reflect"

	"github.com/go-spring/gs-http-gen/lib/httputil"
	"github.com/go-spring/gs-mock/gsmock"
)

var clientType = reflect.TypeFor[Client]()

// Client is a wrapper struct that embeds ClientInterface.
// It can be used to track or extend the construction process of the client.
type Client struct {
	Transport   httputil.Transport
	ServiceName string
}

{{- range $r := .RPCs}}
	{{- if $r.Comment}}
{{$r.Comment}}
	{{- end}}
{{- $respType := "" }}
{{- if $r.Stream}}
	{{- $respType = "httputil.Stream" }}
{{- else}}
	{{- $respType = $r.Response }}
{{- end}}
func (c *Client) {{$r.Name}}(ctx context.Context, req *{{$r.Request}}, opts ...httputil.RequestOption) (*http.Response, *{{$respType}}, error) {
	if ret, ok := gsmock.InvokeContext(ctx, clientType, "{{$r.Name}}", ctx, req, opts); ok {
		return gsmock.Unbox3[*http.Response, *{{$respType}}, error](ret)
	}
	q, err := req.FormValues()
	if err != nil {
		return nil, nil, err
	}
	path := fmt.Sprintf("{{$r.FormatPath}}", {{- range $p := $r.PathParams}} req.{{$p}}, {{- end}})
	if len(q) > 0 {
		path += "?" + q.Encode()
	}
	r, err := httputil.NewRequest(ctx, "{{$r.Method}}", path, nil)
	if err != nil {
		return nil, nil, err
	}
	r.Header.Set("Content-Type", "{{$r.ContentType}}")
	conn, err := c.Transport.GetConn(c.ServiceName, "http")
	if err != nil {
		return nil, nil, err
	}
	{{- if $r.Stream}}
		return httputil.StreamResponse(conn, r, path, opts...)
	{{- else}}
		return httputil.JSONResponse[{{$r.Response}}](conn, r, path, opts...)
	{{- end}}
}
{{end}}
`))

// genClient generates the HTTP client code for a given service.
func (g *Generator) genClient(config *generator.Config, rpcs []RPC) error {
	buf := &bytes.Buffer{}
	err := clientTmpl.Execute(buf, map[string]any{
		"Package": config.GoPackage,
		"RPCs":    rpcs,
	})
	if err != nil {
		return errutil.Explain(nil, "execute template error: %w", err)
	}
	fileName := filepath.Join(config.OutputDir, "client.go")
	return g.FormatFile(fileName, buf.Bytes())
}
