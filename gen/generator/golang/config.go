package golang

import (
	"bytes"
	"path/filepath"
	"text/template"

	"github.com/go-spring/gs-http-gen/gen/generator"
	"github.com/go-spring/stdlib/errutil"
	"github.com/go-spring/stdlib/jsonflow"
)

// configTmpl is the Go source code template for generating the configuration.
var configTmpl = template.Must(template.New("config").Parse(`
// Code generated by gs-http-gen ({{.ToolVersion}}) compiler. DO NOT EDIT.

package {{.Package}}

const Config = ` + "`{{.Config}}`" + `
`))

// genConfig generates a Go source file that contains the user configuration.
func (g *Generator) genConfig(config *generator.Config, spec GoSpec) error {
	b, err := jsonflow.MarshalIndent(spec.Meta.Config, "", "    ")
	if err != nil {
		return errutil.Explain(err, "marshal meta config error")
	}
	buf := &bytes.Buffer{}
	err = configTmpl.Execute(buf, map[string]any{
		"Package":     config.GoPackage,
		"Config":      string(b),
		"ToolVersion": config.ToolVersion,
	})
	if err != nil {
		return errutil.Explain(err, "execute config template error")
	}
	fileName := filepath.Join(config.OutputDir, "config.go")
	return formatFile(fileName, buf.Bytes())
}
